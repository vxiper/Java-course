package src;

public class dataTypes {
    public static void main(String[] args) {
        boolean number0 = false;//true ya da false
        byte number1 = 127;//-128 ile 127 arası
        char karakter = 'x';//karakter
        short number2 = 32767;//tam sayı -32,768 ile 32,767 arası
        int number3 = 2147483647;//tam sayı -2,147,483,648 ile 2,147,483,647 arası
        long number4 = 9223372036854775807L;//tam sayı -9,223,372,036,854,775,808 ile 9,223,372,036,854,775,807 arası
        float number5 = 312312312.2f;//ondalıklı sayı 7 decimal
        double number6 = 12312312312313123.5d;//ondalıklı sayı 16 decimal

        System.out.println(number0);
        System.out.println(number1);
        System.out.println(karakter);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(number4);
        System.out.println(number5);
        System.out.println(number6);
    }
}
